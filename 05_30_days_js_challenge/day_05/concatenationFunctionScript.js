// Function expression to concatenate two strings
const concatenateStrings = function (str1, str2) {
  return str1 + str2;
};

// Example usage
const string1 = "Hello, ";
const string2 = "world!";
const result = concatenateStrings(string1, string2);
console.log(result); // Output: Hello, world!

const string3 = "Open";
const string4 = "AI";
const result2 = concatenateStrings(string3, string4);
console.log(result2); // Output: OpenAI

// Number;

// Definition:
// The number data type in JavaScript is used to store numeric values, including integers and floating-point numbers.

// Integer
let integerNumber = 42;

// Floating-point number
let floatNumber = 3.14;

// Operations
let sum = integerNumber + floatNumber; // 45.14
let product = integerNumber * floatNumber; // 131.88

// Output
console.log("Sum:", sum);
console.log("Product:", product);

// In this example:

// integerNumber stores an integer value (42).

// floatNumber stores a floating-point value (3.14).

// The sum variable holds the result of adding the two numbers.

// The product variable holds the result of multiplying the two numbers.

// JavaScript automatically handles numeric operations, and you can perform various mathematical operations using numbers. It's important to note that JavaScript does not distinguish between integers and floating-point numbers when performing operations; it treats all numbers as floating-point.

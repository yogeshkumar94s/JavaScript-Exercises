// Expressions:

// An expression is any valid unit of code that resolves to a value. It can be a combination of variables, operators, and literals. Expressions produce a value.

// Examples of expressions:

let sum = 5 + 3; // Arithmetic expression
let greeting = "Hello"; // String expression
let isTrue = true; // Boolean expression
let value = 42; // Numeric expression

// In each of these examples, the combination of variables, literals, and operators results in a value.

// Statements:

// A statement is a larger unit of code that performs an action. It doesn't necessarily produce a value. Statements are executed one after another and often end with a semicolon (;).

// Examples of statements:

let x = 5; // Variable declaration statement
console.log("Hello"); // Function call statement
if (x > 0) {
  // If statement
  console.log("Positive");
} else {
  console.log("Non-positive");
}

// In these examples, you can see various types of statements. Variable declaration (let x = 5;), function call (console.log("Hello");), and control flow statements (if, else) are all examples of statements.

// Expression Statements:

// Sometimes, expressions can stand alone as statements. These are called expression statements.

// Example:

let result = add(2, 3); // Function call expression used as a statement

// Here, add(2, 3) is an expression that also stands alone as a statement because it has an effect (it calls the add function).

// In summary, expressions produce values, while statements perform actions. JavaScript code is generally a combination of both expressions and statements working together to achieve specific tasks. Understanding these distinctions is crucial for writing effective and meaningful code.

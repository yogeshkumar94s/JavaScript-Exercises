// Arrow function expression to calculate the sum of two numbers
const calculateSum = (num1, num2) => num1 + num2;

// Example usage
const number1 = 5;
const number2 = 3;
const result = calculateSum(number1, number2);
console.log(`The sum of ${number1} and ${number2} is ${result}.`); // Output: The sum of 5 and 3 is 8.

const number3 = 10;
const number4 = 15;
const result2 = calculateSum(number3, number4);
console.log(`The sum of ${number3} and ${number4} is ${result2}.`); // Output: The sum of 10 and 15 is 25.

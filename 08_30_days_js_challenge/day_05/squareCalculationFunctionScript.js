// Function to calculate the square of a number
function calculateSquare(number) {
  return number * number;
}

// Example usage
const number1 = 5;
const square1 = calculateSquare(number1);
console.log(`The square of ${number1} is ${square1}.`); // Output: The square of 5 is 25.

const number2 = 8;
const square2 = calculateSquare(number2);
console.log(`The square of ${number2} is ${square2}.`); // Output: The square of 8 is 64.

const number3 = -3;
const square3 = calculateSquare(number3);
console.log(`The square of ${number3} is ${square3}.`); // Output: The square of -3 is 9.

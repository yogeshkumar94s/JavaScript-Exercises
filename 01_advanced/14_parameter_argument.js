// Parameter:

// A parameter is a variable or placeholder in a function definition.
// It is a symbolic name for a piece of data that a function expects to receive as input.
// Parameters are defined within the parentheses following the function name.
// They act as local variables inside the function.

// Example:
function add(x, y) {
  // x and y are parameters
  return x + y;
}

//   Argument:

//   An argument is a specific value or expression passed to a function when it is invoked (called).
//   Arguments are the actual values that fulfill the parameters' expectations.
//   When you call a function, you provide arguments that match the order and number of parameters defined in the function.

//   Example:
const result = add(3, 4);
// 3 and 4 are arguments
// Here, 3 and 4 are arguments passed to the add function. The function will use these values to perform the operation defined in its body.

//In summary, parameters are the placeholders in a function definition, and arguments are the actual values passed to a function when it is called. The terms help distinguish between the abstract input expected by a function and the concrete values supplied during its invocation.
